    private void sourceforgeLinkMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_sourceforgeLinkMouseClicked
    {//GEN-HEADEREND:event_sourceforgeLinkMouseClicked
        if (Desktop.isDesktopSupported())
        {
            desktop = Desktop.getDesktop();
            try
            {
                desktop.browse(URI.create("http://sourceforge.net/p/minecraft-backup-and-restore"));
            } catch (Exception ex)
            {
                debug.printDebugInfo("Error: On opening a link: " + ex);
            }
        }
    }//GEN-LAST:event_sourceforgeLinkMouseClicked

